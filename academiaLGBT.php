        <web-header3>Abstract</web-header3>

        <web-body>
            <br> &emsp; In America, the LGBTQIA+ Movement (simplified to LGBT+) is a famous organization that 
        comprises of misrepresented sexual orientations and gender identities. The movement has been 
        successful in America for establishing support groups that mitigate LGBT+ isolation, pressuring 
        lawmakers  into  providing  LGBT+  with  legal  protections,  and  the  decrease  of  LGBT+ 
        discrimination in the United States. The movement was essential in establishing protections for 
        the future generations which definitely benefit from their sacrifices to this day. However, the 
        acceptance of LGBT+ is  disproportionally accepted across the world. Some nations outright 
        consider homosexuality a capital crime, while other nations keep LGBT+ in a segregated state. 
        Unfortunately for Japan, they fall into this classification of a nation. Therefore, it is worth studying 
        the state of LGBT+ protection in Japan for sake of determining the welfare of LGBT+ individuals 
        in Japan. This paper aims to investigate the paradigms of Japanese LBGT+ movements, their 
        fights for equality, and establish a clear standing of Japanese treatment of LGBT+.
