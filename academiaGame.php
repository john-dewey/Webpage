    <div class="project-description">
        <!-- Abstract -->
        <web-header3>Abstract</web-header3>

        <web-body>
            <br> &emsp; Orientalism is a concept defined by Said which is “a style of thought based upon an 
            ontological and epistemological distinction made between 'the orient' and 'the occident'” (Said, 
            2). Orientalism consists of distinctions that generalize the Occident and Orient's nature of being 
            or way of knowledge. These distinctions are made to separate any connection between the  Orient 
            and the Occident. The media uses Orientalism to make unfair characterizations of Asia. In video 
            games this is especially true; Orientalism is used to negatively characterize American enemies in 
            Asia. Consumers play these games and are presented with Orientalist tropes that may lead them to  
            believe that America is justified in its foreign policy. In this paper, I will explain how video games 
            can be used as a tool to help justify American foreign policy. Then, I will propose research which 
            I wish to conduct on video games.
        </web-body>
